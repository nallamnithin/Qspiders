//print the odd numbers from 20 to 1.
class  LoopOddNumber
{
	public static void main(String[] args) 
	{
		for (int i =20;i>=1;i--)
		{
			if (i%2!=0)//By using Decision statements
			{
				System.out.println(i);
		  }
	   }
	   System.out.println("==============================================");
    
	for (int i=19;i>=1 ;i=i-2)//withot using decision statements
		System.out.println(i);
	}
 }
